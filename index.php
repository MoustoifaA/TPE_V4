              <h3 class="text-center"> <u>Les D&eacute;buts du Vol </u></h3>
              <p>&nbsp;&nbsp;Le vol libre a &eacute;t&eacute; initi&eacute; par <a href="https://fr.wikipedia.org/wiki/Otto_Lilienthal">Otto Lilienthal</a> (1848-1896). Ce constructeur de planeurs r&eacute;alisa de nombreux vols sur
                des engins particuli&egrave;rement bien con&ccedil;us. Scientifique, il construisait, modifiait et notait le fruit
                de ses recherches. Il construisait des planeurs &agrave; ailes de type delta afin d&rsquo;assurer une bonne
                r&eacute;partition autour du centre de gravit&eacute;. Il assurait le contr&ocirc;le de l&rsquo;appareil par le
                mouvement du corps.</p>
              <p>Lilienthal fut un pr&eacute;curseur et influen&ccedil;a d&rsquo;autres chercheurs tels que Percy
                Pilcher (1866-1899) ou Octave Chanute (1832-1910). Ce dernier, un constructeur am&eacute;ricain, publia un recueil
                intitul&eacute; &laquo; Progr&egrave;s &raquo; dans les machines volantes. Son livre, ses conseils et son
                amiti&eacute; inspira les fr&egrave;res Orville et Wilbur Wright.</p>
              <p>Beaucoup d&rsquo;autres personnes ont permis de faire &eacute;voluer les technologies de vol.
                Parmi ceux-ci, on peut citer l&rsquo;Allemand Gottlieb Daimler qui r&eacute;alisa le premier moteur d&rsquo;avion
                en 1885. Il s&rsquo;agissait d&rsquo;un monocylindre &agrave; combustion interne. Il s&rsquo;inspirait des moteurs
                &agrave; quatre temps invent&eacute;s par Nicholas Otto et utilisait l&rsquo;essence comme carburant.</p>
              <p>Le premier vol motoris&eacute;, contrairement &agrave; ce qu&rsquo;on pense, ne fut pas
                r&eacute;alis&eacute; par Cl&eacute;ment Ader en 1890. L&rsquo;Eole (nom de l'avion) ne fit que se soulever mais
                ne vola pas. Cette nuance n&rsquo;enl&egrave;ve rien &agrave; l&rsquo;exploit qu&rsquo;a r&eacute;alis&eacute; le
                pionnier fran&ccedil;ais.</p>
              <p>L&rsquo;Am&eacute;ricain Samuel Pierpont Langley fut celui qui faillit devancer les fr&egrave;res
                Wright. Il collabora avec Charles Manley et Stephen Balzer pour faire voler un avion baptis&eacute;
                l&rsquo;a&eacute;rodrome, &eacute;quip&eacute; d&rsquo;un remarquable moteur rotatif en &eacute;toile &agrave;
                cinq cylindres. En d&eacute;pit de cette technologie avanc&eacute;e, l&rsquo;avion de Langley ne vola jamais. Deux
                essais, le 7 octobre et le 8 d&eacute;cembre se termin&egrave;rent dans le Potomac. Les observateurs
                rapport&egrave;rent que l&rsquo;avion se lib&eacute;ra mal de sa rampe de lancement. Certains ont avanc&eacute;
                que la structure de l&rsquo;appareil se d&eacute;sint&eacute;gra au d&eacute;collage.</p>
              <p>L&rsquo;exploit tant attendu eut lieu le jeudi 17 d&eacute;cembre 1903. Les fr&egrave;res Wright
                ont m&eacute;rit&eacute; leurs succ&egrave;s, gr&acirc;ce &agrave; leur d&eacute;termination &agrave; surmonter
                toutes les difficult&eacute;s qu&rsquo;ils rencontraient. Lorsque le &laquo; Flyer &raquo; fut
                d&eacute;mont&eacute; apr&egrave;s ce jour historique, c&rsquo;&eacute;tait la fin de la premi&egrave;re phase de
                vol motoris&eacute;. Trois autres vols survirent, le dernier fut le plus performant avec une distance couverte de
                260 m&egrave;tres, le gouvernail de profondeur fut alors endommag&eacute; &agrave; l&rsquo;atterrissage. Ces voles
                eurent lieu sans t&eacute;moin et le monde ne fut pas inform&eacute; qu&rsquo;un homme avait vol&eacute; dans une
                machine plus lourde que l&rsquo;air.</p>
              <p>Ce n&rsquo;est pas avant novembre 1906, trois ann&eacute;es plus tard, que le Br&eacute;silien
                Albertos Santos-Dumont r&eacute;volutionna l&rsquo;Europe en faisant voler son &laquo; 14+ bis &raquo; sur 61
                m&egrave;tres.</p>
              <p>18 mois apr&egrave;s le premier vol des fr&egrave;res Wright, une am&eacute;lioration du Flyer
                boucle 40 km. Dor&eacute;navant leur machine ob&eacute;it au doigt et &agrave; l'oeil. De peur de la concurrence
                ils taisent leur performances mais lorsqu'ils d&eacute;cident de faire un vol en public les Fran&ccedil;ais sont
                entr&eacute;s en lice.</p>
              <p>Peu &agrave; peu on oublie les biplans par manque de vitesse et certains hommes comme
