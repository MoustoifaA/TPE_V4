                  les filets de l'&eacute;coulement ne sont plus lisses. Il est essentiel que la tra&icirc;n&eacute;e de forme
                  soit r&eacute;duite au minimum sur toutes les parties de l'avion qui sont expos&eacute;es &agrave; l'air</li>
                <li>Train&eacute;e &laquo;&nbsp;Profil&nbsp;&raquo;&nbsp;: La tra&icirc;n&eacute;e de forme et la
                  tra&icirc;n&eacute;e de frottement constituent une grande partie de la tra&icirc;n&eacute;e totale d'un avion
                  Parfois, on donne &agrave; la somme de ces deux tra&icirc;n&eacute;es le nom de tra&icirc;n&eacute;e de profil
                </li>
                <li><img src="Images/Physique de l'avion_clip_image011.jpg" alt="rererer" width="335" height="205" hspace="12"
                    align="left" />Train&eacute; &laquo;&nbsp;Induites&nbsp;&raquo;&nbsp;: Toujours dirig&eacute;e
                  parall&egrave;lement au vent relatif. C'est la cause principale des tourbillons de bord de fuite: l'air qui
