                gr&acirc;ce &agrave; la viscosit&eacute; de l&rsquo;ai qui provoque la tra&icirc;n&eacute;e.<br />
                La tra&icirc;n&eacute;e totale d&rsquo;un avion est en fait l&rsquo;addition de 3 tra&icirc;n&eacute;es
                d&rsquo;origines diff&eacute;rentes :
              </p>
              <ul>
                <li>Train&eacute;e &laquo;&nbsp;Frottement&nbsp;&raquo;&nbsp;: Il s&rsquo;agit du frottement de l&rsquo;air sur
                  toute la peau de l&rsquo;avion, sa structure : train d'atterrissage, m&acirc;ts, antennes, etc. Elle augmente du
