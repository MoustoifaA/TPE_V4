    <h3>Engin plus dense que l&rsquo;air</h3>
    <p>&nbsp;&nbsp; Pendant des si&egrave;cles le plus grand r&ecirc;ve de l&rsquo;homme a
      &eacute;t&eacute; de voler, et ce fut leurs but communs mais seulement quelques-uns se sont immortalis&eacute;s
      gr&acirc;ce &agrave; leurs brillantes innovations. En effet, Le premier homme, que l&rsquo;on peut
      consid&eacute;rer comme le p&egrave;re de la navigation a&eacute;rienne fut le <em><a
          href="Infos_Perso/Sir George Cayley.html">Baronet Georges Cayley</a></em> (1773-1857). Il construisit en 1807 ce que l&rsquo;on
      consid&egrave;re comme le premier mod&egrave;le r&eacute;duit de planeur. Il s&rsquo;agissait en fait d&rsquo;un
      manche &agrave; balai sur lequel &eacute;tait mont&eacute;e une aile en forme de cerf-volant. C&rsquo;&eacute;tait
      suffisant pour faire admettre le principe que l'engin ï¿½tait plus lourd que l&rsquo;air.<br />
      A partir de ce mod&egrave;le, il con&ccedil;ut en 1849 un autre planeur capable de transporter un jeune
      gar&ccedil;onnet puis son cochet en 1853. <em>Cayley</em> continua ses recherches jusqu&rsquo;&agrave; sa mort et
      sugg&eacute;ra plusieurs id&eacute;es :</p>
    <p> - utiliser un moteur &agrave; combustion, prouvant qu&rsquo;une surface courbe poss&eacute;dait une
      surface ascensionnelle propre<br />
      - des ailes biplans ou triplans offraient une force ascensionnelle propre optimum pour une structure
      l&eacute;g&egrave;re et robuste.</p>
    <p>Bien qu&rsquo;&agrave; cette &eacute;poque, on savait &agrave; peu pr&egrave;s construire un engin
      capable de voler, le probl&egrave;me du pilotage demeurait entier : le moteur appropri&eacute; restait &agrave;
      inventer. Il faut enfin exp&eacute;rimenter en 1876 par l&rsquo;ing&eacute;nieur allemand <em><a
          href="Infos_Perso/Nicholas Otto.html">Nicholas Otto</a></em>.</p>
    <p> Mais l&rsquo;invention d&rsquo;<em>Otto</em> appartenait au futur. Les contraintes de
      l&rsquo;&eacute;poque permirent de d&eacute;velopper l&rsquo;a&eacute;rodynamisme de la carcasse des appareils
      pour essayer de d&eacute;couvrir le moyen de contr&ocirc;ler leurs engins dans les airs.</p>
