                sont attir&eacute;s vers le centre de la Terre.<br />
                En pratique, on peut admettre que le poids s'exerce toujours selon une ligne qui va du centre de gravit&eacute; de
                l'avion au centre de la Terre. On pourrait croire &agrave; premi&egrave;re vue que le poids change seulement en
                fonction de la consommation de carburant. En r&eacute;alit&eacute;, lorsqu'un avion man&oelig;uvre, il subit des
                variations de son facteur de charge ou des G qui modifient la charge support&eacute;e par les ailes. Pour
