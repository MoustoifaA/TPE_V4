              <ul>
                <li><strong><u>Evolution selon 3 Axes</u></strong></li>
              </ul>
              <p>Le contr&ocirc;le d'un avion se fait gr&acirc;ce &agrave; des parties mobiles au niveau des ailes et de la queue;
                ailerons, volets, a&eacute;rofreins, gouvernes de direction, gouvernes de profondeur, assurent la stabilit&eacute;
                d'un avion en vol et permettent de la diriger.<br />
                En effet, Avion &eacute;volue selon 3 axes&nbsp;:<strong> </strong> <br />
                <img width="252" height="176" src="Images/Physique de l'avion_clip_image002.gif" align="left" hspace="12"
                  alt="schéma avion tangage lacet roulis" /><img
                  src="Images/Physique de l'avion_clip_image004.gif" alt="fleche rouge" width="11" height="11" />&nbsp;<strong>La
                  gouverne de profondeur : le tangage</strong><br />
                L'axe de tangage passe d'une extr&eacute;mit&eacute; d'aile &agrave; l'autre. La rotation autour de l'axe est
                obtenue par une action sur la commande de profondeur. <br />
                Lorsque le manche est pouss&eacute; vers l'avant, la gouverne de profondeur s'abaisse. Le plan horizontal voit
                alors sa portance augmenter, celle-ci cr&eacute;ant un moment de tangage faisant baisser le nez de l'appareil
                (&agrave; piquer). Inversement, tir&eacute; en arri&egrave;re, le manche provoque la rotation vers le haut de la
                gouverne, ce qui g&eacute;n&egrave;re un moment de tangage &agrave; cabrer. La gouverne de profondeur joue donc
                sur l'assiette.<br />
                <img src="Images/Physique de l'avion_clip_image004_0000.gif" alt="ererer" width="11"
                  height="11" />&nbsp;<strong>Les ailerons : le roulis</strong><br />
                L'axe de roulis passe du nez &agrave; la queue de l'avion. La rotation autour de l'axe est obtenue par une action
                sur la commande de gauchissement (manche &agrave; gauche ou &agrave; droite). <br />
                La portance augmente du cot&eacute; de l'aileron baiss&eacute; et diminue du cot&eacute; de l'aileron lev&eacute;
                : l'avion s'incline du cot&eacute; du manche. En jouant sur l'inclinaison, les ailerons induisent une inclinaison
                de la portance, et provoque le virage.<br />
                <img src="Images/Physique de l'avion_clip_image004_0001.gif" alt="erererrere" width="11"
                  height="11" /><strong>&nbsp;</strong><strong>La gouverne de direction : le lacet</strong> <br />
                L'axe de lacet est perpendiculaire aux 2 autres, entre par la t&ecirc;te du pilote et sort par son si&egrave;ge.
                La rotation autour de cet axe est obtenue par une action sur le palonnier. Il permet de maintenir le plan de
                sym&eacute;trie de l'avion dans le vent relatif. La gouverne de direction se d&eacute;place dans le sens o&ugrave;
                le palonnier est pouss&eacute;.<br />
                Lorsque les filets d'air autour du fuselage ne sont pas sym&eacute;triques, il y d&eacute;rapage. Le
                d&eacute;rapage est l'angle form&eacute; par l'axe longitudinal de l'avion et par le vent relatif. A faible
                vitesse il peut &ecirc;tre dangereux. Le d&eacute;rapage d'un avion ayant un di&egrave;dre positif entra&icirc;ne
                un roulis.
              </p>
